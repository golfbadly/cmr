package com.xplink.random_cm.domainbean;

public class KeywordBean {
	
	private int keywordId;
	private String inkeyword;
	private String outkeyword;
	private String year ;
	private int eventDetailId;
	
	public int getKeywordId() {
		return keywordId;
	}
	public void setKeywordId(int keywordId) {
		this.keywordId = keywordId;
	}
	public String getInkeyword() {
		return inkeyword;
	}
	public void setInkeyword(String inkeyword) {
		this.inkeyword = inkeyword;
	}
	public String getOutkeyword() {
		return outkeyword;
	}
	public void setOutkeyword(String outkeyword) {
		this.outkeyword = outkeyword;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public int getEventDetailId() {
		return eventDetailId;
	}
	public void setEventDetailId(int eventDetailId) {
		this.eventDetailId = eventDetailId;
	}



}
